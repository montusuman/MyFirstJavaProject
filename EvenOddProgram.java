
//Print even and odd number betwwen 1 to 10
public class EvenOddProgram {

    public static void main(String[] args){
        for(int i=1; i<11;i++){
            if(i%2 == 0){
                System.out.println("Even Numbers are: " +i);
            }
            else {
                System.out.println("Odd numbers are: " + i);
            }
        }
    }
}
